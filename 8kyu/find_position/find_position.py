def position(alphabet):
    alpha_list = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l',
                  'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x',
                  'y', 'z']
    alphabet.lower()
    if alphabet in alpha_list:
        index = (alpha_list.index(alphabet)) + 1
        return f'Position of alphabet: {index}'


